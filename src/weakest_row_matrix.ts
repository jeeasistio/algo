const weakestRow = (grid: number[][]) => {
  let sldrsPerRow: number[] = []

  for (let i = 0; i < grid.length; i++) {
    const arr = grid[i]
    const arrLength = arr.length
    let left = 0
    let right = arrLength - 1
    let firstZeroIndex: number

    while (left <= right) {
      let middle = Math.round((left + right) / 2)

      if (arr[middle] === 0) {
        firstZeroIndex = middle
        right = middle - 1
      } else {
        left = middle + 1
      }
    }

    sldrsPerRow.push(firstZeroIndex)
  }

  return Math.min(...sldrsPerRow)
}

const weakestRow2 = (grid: number[][]) => {
  let sldrsPerRow: number[] = []

  for (let i = 0; i < grid.length; i++) {
    const arr = grid[i]
    let count = 0

    for (let j = 0; j < arr.length; j++) {
      if (arr[j] === 0) break
      count++
    }

    sldrsPerRow.push(count)
  }

  return Math.min(...sldrsPerRow)
}

